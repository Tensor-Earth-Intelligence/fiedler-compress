r"""
Generate benchmarks/SWEEP.md from the raw sweep JSON in benchmarks/results/.

This is the "generating script" layer of the three-layer publishing structure for the
PRIMARY benchmark: the multi-passage SQuAD compression-ratio sweep. The summary is
GENERATED from the raw per-item scores (not hand-asserted), so it cannot silently drift
from the data. Re-run and diff SWEEP.md; if it changes, the summary was stale.

Self-contained on purpose (this is the public repo): depends only on numpy + stdlib, and
imports nothing from any private research repo. The raw JSON carries, for every
(model, condition), the per-item EM survival rate and mean token-F1 plus the per-item
achieved compression and the mem-leak item list -- everything needed to independently
recompute the means, the bootstrap CIs, and the mem-leak-filtered numbers below.

    python benchmarks/generate_sweep_summary.py

Requires: pip install numpy
"""
from __future__ import annotations
import json, pathlib
import numpy as np

HERE = pathlib.Path(__file__).resolve().parent
RAW = HERE / "results" / "survival_sweep_multipassage_20260714.json"
NBOOT = 10000
BOOT_SEED = 20260714


def bootstrap_ci(arr, nboot=NBOOT, seed=BOOT_SEED, alpha=0.05):
    """Percentile bootstrap 95% CI on the mean of a per-item score array. Seeded, so the
    interval is reproducible bit-for-bit."""
    a = np.asarray(list(arr), dtype=float)
    if a.size == 0:
        return (float("nan"), float("nan"), float("nan"))
    rng = np.random.default_rng(seed)
    reps = a[rng.integers(0, a.size, size=(nboot, a.size))].mean(axis=1)
    return (float(a.mean()),
            float(np.percentile(reps, 100 * alpha / 2)),
            float(np.percentile(reps, 100 * (1 - alpha / 2))))


def _pct(x):
    return f"{x*100:.1f}%"


def _row(cond, recs, leak):
    em_all = [r["em_rate"] for r in recs]
    f1_all = [r["f1_mean"] for r in recs]
    em_flt = [r["em_rate"] for r in recs if r["qid"] not in leak]
    f1_flt = [r["f1_mean"] for r in recs if r["qid"] not in leak]
    ach = [r["achieved_tok_ratio"] for r in recs if r.get("achieved_tok_ratio") is not None]
    achm = (sum(ach) / len(ach)) if ach else None
    fac = (1.0 / (1.0 - achm)) if (achm is not None and achm < 1) else None
    em_m, em_lo, em_hi = bootstrap_ci(em_all)
    ef_m, _, _ = bootstrap_ci(em_flt)
    f1_m, f1_lo, f1_hi = bootstrap_ci(f1_all)
    ff_m, _, _ = bootstrap_ci(f1_flt)
    facs = f"{fac:.2f}x" if fac else "1.00x"
    achs = _pct(achm) if achm is not None else "--"
    return (f"| {cond} | {facs} | {achs} | "
            f"{_pct(em_m)} [{_pct(em_lo)}, {_pct(em_hi)}] | {_pct(ef_m)} | "
            f"{f1_m:.3f} [{f1_lo:.3f}, {f1_hi:.3f}] | {ff_m:.3f} |")


def main():
    d = json.load(open(RAW, encoding="utf-8"))
    cfg = d["config"]
    prov = d["provenance"]
    conds = cfg["conditions"]
    order = [c for c in conds if c != "nocontext"] + ["nocontext"]

    parts = [f"""# fiedler-compress — primary benchmark: multi-passage compression sweep

Generated by `benchmarks/generate_sweep_summary.py` from
`benchmarks/results/survival_sweep_multipassage_20260714.json`.
Re-run that script to regenerate this file; if it changes, this summary was stale.
**Do not hand-edit the tables below.** (Secondary cross-architecture pilot numbers, incl.
the 70–120B class, live in `README.md`.)

## What this measures

Answer **survival under compression**: for each item the model answers a question from a
document, at a range of compression levels; survival is the fraction of items still
answered correctly. The point of interest is how survival falls as more of the context is
removed, and whether pinning the answer-bearing content recovers it.

## Methodology

- **Corpus:** multi-passage SQuAD v1.1 (CC BY-SA), **N={cfg['n_samples']}** items, seed
  {cfg['seed']}. Each item is **k={cfg.get('k_passages')}** concatenated SQuAD paragraphs
  (one gold-answer-bearing + distractors; distractors that contain the gold span are
  rejected). Single SQuAD paragraphs are too short to exercise a real 1.5–5x sweep (they
  fall below the compressor's min-chunk floor and pass through unmodified), so passages are
  concatenated to give the method enough chunks to compress.
- **Compression sweep:** `target_ratio` in {cfg['ratio_sweep']} (fraction removed). Because
  removal is whole-chunk, the **achieved** compression can plateau below the nominal target;
  we therefore report **achieved** token compression and factor per condition, and adjacent
  nominal targets can collapse to one achieved level. `cpin{int(cfg['cpin_ratio']*100)}` =
  most-aggressive ratio with the gold answer phrase content-**pinned** (never removed).
- **Two deterministic evaluators** (no LLM judge), scored on the same outputs:
  **EM** = normalized substring/exact-match containment of a gold span (binary); **F1** =
  official SQuAD v1.1 token-level F1 (continuous). EM requires the full gold span verbatim,
  so it is a conservative lower bound that undercounts concise answers; F1 is the
  article/punctuation-robust canonical metric. Both are reported.
- **Mem-leak control:** every question is also asked with **no** document. Items answered
  correctly with no context test training memory, not retrieval; both raw and mem-leak-
  **filtered** numbers are shown.
- **Statistics:** percentile bootstrap **95% CI** ({NBOOT:,} seeded resamples) on the
  per-item score array for each (model, condition, evaluator).
- **Provenance:** host `{prov.get('hostname')}`, fiedler-compress
  `{prov.get('fiedler_optimizer_version')}`, code fingerprint
  `{str(prov.get('code_fingerprint_sha256'))[:12]}`, {cfg['n_reps']} reps/item, temp
  {cfg['temperature']}.

## Results

Columns: **factor** and **achieved** = mean realized token compression; **EM raw [95% CI]**
and **F1 raw [95% CI]** over all items; **EM filt** / **F1 filt** = mem-leak-filtered means.
"""]

    for model, byc in d["per_item"].items():
        leak = set(d.get("memleak", {}).get(model, []))
        parts.append(f"\n### {model}  (mem-leak items: {len(leak)}/{cfg['n_samples']})\n")
        parts.append("| condition | factor | achieved | EM raw [95% CI] | EM filt | F1 raw [95% CI] | F1 filt |")
        parts.append("|---|---|---|---|---|---|---|")
        for c in order:
            if c in byc and byc[c]:
                parts.append(_row(c, byc[c], leak))

    parts.append("""
## How to read this

- **Survival falls monotonically with compression** under the query-agnostic method, then
- **content-pinning (`cpin`) restores survival to ~baseline even at ~5x compression** —
  a training-free way to protect known-salient content.
- **EM vs F1 gap is model-dependent**: verbose models score EM >> F1 (the gold span is
  present but padded with extra tokens); concise models score EM ≈ F1.
""")

    out = HERE / "SWEEP.md"
    out.write_text("\n".join(parts) + "\n", encoding="utf-8")
    print(f"wrote {out}")


if __name__ == "__main__":
    main()
